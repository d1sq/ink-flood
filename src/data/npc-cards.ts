/** Get NPC card by id */
export function getNpcCard(id: string): NpcCard | undefined {
  return NPC_CARDS.find(c => c.id === id);
}

/** Get NPC card by name (fuzzy: checks if name is contained) */
export function getNpcCardByName(name: string): NpcCard | undefined {
  const lower = name.toLowerCase();
  return NPC_CARDS.find(c =>
    c.name.toLowerCase().includes(lower) || lower.includes(c.name.toLowerCase())
  );
}
