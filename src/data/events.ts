/** Get event definition by ID */
export function getEvent(id: EventId): EventDefinition {
  return EVENTS.find(e => e.id === id)!;
}

/** Get event with cycle-specific overrides applied */
export function getEventForCycle(id: EventId, cycle: number): EventDefinition {
  const base = getEvent(id);
  if (!base.cycleOverrides) return base;

  // Find the highest applicable override (e.g. cycle 9 uses override 9, not 8)
  let override: { description?: string; gmHints?: string } | undefined;
  for (const [c, o] of Object.entries(base.cycleOverrides)) {
    if (cycle >= Number(c)) override = o;
  }

  if (!override) return base;

  return {
    ...base,
    description: override.description ?? base.description,
    gmHints: override.gmHints ?? base.gmHints,
  };
}

/** Format minutes-from-midnight as HH:MM */
export function formatTime(minutes: number): string {
  const h = Math.floor(minutes / 60);
  const m = minutes % 60;
  return `${h.toString().padStart(2, '0')}:${m.toString().padStart(2, '0')}`;
}
